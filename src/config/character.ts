export const preloadCharacterImages = (): Promise<void[]> => {
    const imagePromises = Object.values(characters).map((character) => {
        return new Promise<void>((resolve, reject) => {
            const img = new Image();
            img.onload = () => resolve();
            img.onerror = () => reject(new Error(`Failed to load ${character.avatar}`));
            img.src = character.avatar;
        });
    });

    return Promise.all(imagePromises);
};

